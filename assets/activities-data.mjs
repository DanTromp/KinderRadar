// Single source of truth for KinderRadar activity listings.
//
// Required fields per activity:
//   slug, name, section, category, ageRange, ageMin, ageMax, town,
//   timing, cost, beginnerFriendly, lastVerified
//
// All other fields are optional and degrade gracefully if missing.
// Run `npm run build` to validate the shape of every entry.
//
// Curation notes (first Haltern release):
// - All entries are editor-curated from publicly listed local organizations
//   (Vereine, Stadt Haltern, Pfarrei, Stadtbücherei, RVR/Naturparkhaus, …).
// - `sourceUrl` points to the official organizer page so a parent (or the
//   organizer themselves) can verify, then file a "Suggest update" or
//   "Confirm still running" issue.
// - Times and prices reflect the typical published offering at curation
//   time; where exact figures aren't published, optional fields are omitted
//   rather than guessed (see plan §1 data-hygiene rules).
// - All entries start `verifiedBy: 'editor'`. When an organizer claims a
//   listing (issue template `organizer-claim`), the editor flips it to
//   'organizer' in the next data PR.

